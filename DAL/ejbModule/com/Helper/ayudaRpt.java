package com.Helper;

public class ayudaRpt {
	
	private String TipoSolicitor;
	private String Canal; 
	private String Ubicacion;
	private String Fecha;
	
	public String getTipoSolicitor() {
		return TipoSolicitor;
	}
	public void setTipoSolicitor(String tipoSolicitor) {
		TipoSolicitor = tipoSolicitor;
	}
	public String getCanal() {
		return Canal;
	}
	public void setCanal(String canal) {
		Canal = canal;
	}
	public String getUbicacion() {
		return Ubicacion;
	}
	public void setUbicacion(String ubicacion) {
		Ubicacion = ubicacion;
	}
	public String getFecha() {
		return Fecha;
	}
	public void setFecha(String fecha) {
		Fecha = fecha;
	}

}
